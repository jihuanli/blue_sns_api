	public final static HashMap<String, String>  emojiDict=new HashMap<String, String>();
	public final static HashMap<String, String>  hexDict=new HashMap<String, String>();
			
	private static final String iosEmojiUnicode = "[\ud83c\udc00-\ud83c\udfff]|[\ud83d\udc00-\ud83d\udfff]|[\u2600-\u27ff]"
			+ // ios
			"|[\ue001-\ue05a]|[\ue101-\ue15a]|[\ue201-\ue253]|[\ue301-\ue34d]|[\ue401-\ue44c]|[\ue501-\ue537]";// android

	public static String cleanEmoji(String content, String replacement) {
		if (content != null) {
			if (replacement == null) {
				replacement = "";
			}
			return content.replaceAll(iosEmojiUnicode, replacement);
		}
		return content;
	}

	public static String cleanEmoji(String content) {
		return cleanEmoji(content, "");
	}
	
	public static String filterEmoji(String content, String replacement) {
		return encodeEmoji(content);
	}

	
	public static String encodeEmoji(String content){
		if(content!=null){
			Set<String> hexSourceSet=hexDict.keySet();
			for (String hexSource : hexSourceSet) {
				content=content.replace(hexSource, hexDict.get(hexSource));
			}
			content=cleanEmoji(content);
		}
		return content;
	}
	
	public static String decodeEmoji(String content){
		if(content!=null){
			Set<String> emojiSourceSet=emojiDict.keySet();
			for (String emojiSource : emojiSourceSet) {
				content=content.replace(emojiSource, emojiDict.get(emojiSource));
			}
		}
		return content;
	}
	
